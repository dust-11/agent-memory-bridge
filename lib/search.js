/**
 * 获取锚点及其关联的所有记忆
 * @param {string} anchorId
 * @returns {object|null} { anchor, memories: [] }
 */
function getAnchor(anchorId) {
  const db = getDb();

  const anchor = db.prepare('SELECT * FROM anchors WHERE anchor_id = ?').get(anchorId);
  if (!anchor) return null;

  const memories = db.prepare(`
    SELECT m.* FROM memories m
    JOIN anchor_refs r ON m.id = r.memory_id
    WHERE r.anchor_id = ?
    ORDER BY m.timestamp DESC
  `).all(anchorId);

  return {
    anchor,
    memories: memories.map(parseRow),
  };
}

/**
 * 获取锚点关联的记忆（仅列表）
 * @param {string} anchorId
 * @returns {object[]}
 */
function getAnchorMemories(anchorId) {
  const db = getDb();
  const rows = db.prepare(`
    SELECT m.* FROM memories m
    JOIN anchor_refs r ON m.id = r.memory_id
    WHERE r.anchor_id = ?
    ORDER BY m.timestamp DESC
  `).all(anchorId);
  return rows.map(parseRow);
}

/**
 * 列出所有锚点
 * @param {object} [opts] - { type, limit }
 * @returns {object[]}
 */
function listAnchors(opts = {}) {
  const db = getDb();
  const conditions = [];
  const params = [];

  if (opts.type) {
    conditions.push('type = ?');
    params.push(opts.type);
  }

  const whereClause = conditions.length > 0 ? `WHERE ${conditions.join(' AND ')}` : '';
  const limit = opts.limit || 50;

  const rows = db.prepare(`
    SELECT a.*, COUNT(r.memory_id) as ref_count
    FROM anchors a
    LEFT JOIN anchor_refs r ON a.anchor_id = r.anchor_id
    ${whereClause}
    GROUP BY a.anchor_id
    ORDER BY a.updated_at DESC
    LIMIT ?
  `).all(...params, limit);

  return rows;
}

/**
 * 搜索锚点（基于摘要 + 关键词模糊匹配）
 * @param {string} query
 * @param {object} [opts]
 * @returns {object[]}
 */
function searchAnchors(query, opts = {}) {
  const db = getDb();
  const conditions = ['(summary LIKE ? OR keywords LIKE ?)'];
  const params = [`%${query}%`, `%${query}%`];

  if (opts.type) {
    conditions.push('type = ?');
    params.push(opts.type);
  }

  const limit = opts.limit || 20;
  const rows = db.prepare(`
    SELECT a.*, COUNT(r.memory_id) as ref_count
    FROM anchors a
    LEFT JOIN anchor_refs r ON a.anchor_id = r.anchor_id
    WHERE ${conditions.join(' AND ')}
    GROUP BY a.anchor_id
    ORDER BY a.updated_at DESC
    LIMIT ?
  `).all(...params, limit);

  return rows;
}

