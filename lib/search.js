/**
 * 深度语义检索（TF-IDF 余弦相似度）
 *
 * 当前实现：基于关键词 + 文本重叠的简单评分
 * 升级路径：后期用 embedding 模型替换
 *
 * @param {string} query - 查询文本
 * @param {object} [options] - { limit, type, excludeAutoGenerated }
 * @returns {object[]} 按相似度降序
 */
function searchDeep(query, options = {}) {
  const _noAuto = options.excludeAutoGenerated;
  const db = getDb();
  const { limit = 10, type } = options;

  // 获取候选记录
  const conditions = ["depth IN ('deep', 'archived')"];
  const params = [];

  if (type) {
    conditions.push('type = ?');
    params.push(type);
  }

  const rows = db.prepare(`
    SELECT * FROM memories
    WHERE ${conditions.join(' AND ')}
    ORDER BY timestamp DESC
  `).all(...params);

  // 对每条记录计算与 query 的相似度
  const queryStr = typeof query === 'object' ? (query.keyword || '') : String(query || '');
  const queryTerms = queryStr ? tokenize(queryStr) : [];
  const scored = rows.map(row => {
    const full = `${row.summary} ${row.detail} ${row.keywords}`;
    const docTerms = tokenize(full);
    const score = cosineSimilarity(queryTerms, docTerms);
    return { ...parseRow(row), score };
  });

  // 按分数降序
  scored.sort((a, b) => b.score - a.score);

  let result = scored.slice(0, limit);

  // 后过滤：排除自动生成的对话摘要
  if (_noAuto) {
    result = result.filter(r => {
      const s = (r.summary || '').slice(0, 100);
      return !s.includes('对话摘要');
    });
  }

  return result;
}

