/**
 * 解析行数据：将 keywords 从 JSON 字符串解析为数组
 */
function parseRow(row) {
  if (!row) return null;
  try {
    row.keywords = JSON.parse(row.keywords || '[]');
  } catch {
    row.keywords = [];
  }
  return row;
}

/**
 * 中文分词（基于 nodejieba）
 * 提取有实际意义的中文词 + 英文单词
 */
const STOP_WORDS = new Set([
  "我", "你", "他", "她", "它", "我们", "你们", "他们", "她们", "它们",
  "的", "了", "是", "在", "有", "和", "就", "不", "也", "都", "这", "那",
  "吗", "啊", "呢", "吧", "呀", "哦", "嗯", "哈", "嘿", "哎", "喂",
  "一个", "这个", "那个", "什么", "怎么", "为什么", "因为", "所以",
  "但是", "然后", "而且", "或者", "如果", "虽然", "可以", "没有",
  "不是", "还是", "就是", "只是", "但是", "还是", "觉得", "知道",
  "说", "看", "想", "到", "了", "过", "着", "让", "把", "被", "从",
  "又", "再", "还", "已经", "可能", "应该", "要", "会", "能",
  "跟", "对", "给", "为", "向", "以", "与", "及", "等", "或", "并",
  "上", "下", "中", "前", "后", "里", "外", "多", "少", "很", "太",
  "非常", "比较", "最", "更", "越", "稍", "略", "将", "可",
  "其", "之", "所", "被", "把", "让", "使", "令",
  "的", "地", "得", "着", "过", "了", "呢",
  "去", "来", "做", "搞", "弄", "打",
]);

function tokenize(text) {
  if (!text) return [];
  const terms = [];

  // jieba 中文分词
  const words = nodejieba.cut(text);
  for (const w of words) {
    const trimmed = w.trim();
    // 过滤：停用词、单字（除英文外）、纯标点
    if (!trimmed || trimmed.length < 1) continue;
    if (/^[\u4e00-\u9fff]+$/.test(trimmed)) {
      // 中文词：去掉停用词和单字
      if (trimmed.length >= 2 && !STOP_WORDS.has(trimmed)) {
        terms.push(trimmed);
      }
    } else if (/^[a-zA-Z0-9]+$/.test(trimmed)) {
      // 英文/数字：只保留长度 >= 2 的（跳过单字母、单数字）
      if (trimmed.length >= 2) {
        terms.push(trimmed.toLowerCase());
      }
    }
    // 其他（混合、符号）→ 忽略
  }

  // 额外提取英文单词（处理 jieba 可能漏切的英文连词）
  const engWords = text.toLowerCase().match(/[a-zA-Z][a-zA-Z0-9_-]*/g) || [];
  for (const w of engWords) {
    if (w.length >= 2 && !terms.includes(w)) {
      terms.push(w);
    }
  }

  return terms;
}

/**
 * 计算两个词袋的余弦相似度
 */
function cosineSimilarity(termsA, termsB) {
  if (termsA.length === 0 || termsB.length === 0) return 0;

  // 构建词频向量
  const freqA = {};
  const freqB = {};
  const allTerms = new Set();

  for (const t of termsA) { freqA[t] = (freqA[t] || 0) + 1; allTerms.add(t); }
  for (const t of termsB) { freqB[t] = (freqB[t] || 0) + 1; allTerms.add(t); }

  let dotProduct = 0;
  let magA = 0;
  let magB = 0;

  for (const term of allTerms) {
    const a = freqA[term] || 0;
    const b = freqB[term] || 0;
    dotProduct += a * b;
    magA += a * a;
    magB += b * b;
  }

  if (magA === 0 || magB === 0) return 0;
  return dotProduct / (Math.sqrt(magA) * Math.sqrt(magB));
}

