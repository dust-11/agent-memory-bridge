/**
 * 按 ID 获取单条记忆
 * @param {string} id
 * @returns {object|null} 记忆记录或 null
 */
function getMemory(id) {
  const db = getDb();
  const row = db.prepare('SELECT * FROM memories WHERE id = ?').get(id);
  if (!row) return null;
  return parseRow(row);
}

/**
 * 获取当前所有未过期的记忆（快速总览）
 * @param {object} [opts] - { depth, type, limit }
 */
function getAllMemories(opts = {}) {
  const db = getDb();
  const { depth, type, limit = 50 } = opts;
  const conditions = [];
  const params = [];

  // 默认只查未过期的记录
  conditions.push(`(expire_at IS NULL OR expire_at > datetime('now'))`);

  if (depth) {
    conditions.push('depth = ?');
    params.push(depth);
  }
  if (type) {
    conditions.push('type = ?');
    params.push(type);
  }

  const sql = `SELECT * FROM memories WHERE ${conditions.join(' AND ')} ORDER BY timestamp DESC LIMIT ?`;
  params.push(limit);

  const rows = db.prepare(sql).all(...params);
  return rows.map(parseRow);
}

