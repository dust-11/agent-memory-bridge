/**
 * 条件检索记忆
 *
 * @param {object} filters
 * @param {string}  [filters.type]       - 'core' | 'emotion'
 * @param {string}  [filters.depth]      - 'shallow' | 'deep' | 'archived'
 * @param {string}  [filters.keyword]    - 关键词模糊匹配（搜索 keywords JSON 字段）
 * @param {string}  [filters.source]     - 'openclaw' | 'hermes' | 'manual'
 * @param {string}  [filters.timeFrom]   - ISO8601 起始时间
 * @param {string}  [filters.timeTo]     - ISO8601 截止时间
 * @param {number}  [filters.limit=50]   - 返回条数
 * @param {number}  [filters.offset=0]   - 偏移量（分页）
 * @param {boolean} [filters.includeExpired=false] - 是否包含过期记录
 * @param {string}  [filters.searchText] - 搜索摘要/详情文本（LIKE 模糊匹配）
 * @param {boolean} [filters.excludeAutoGenerated=false] - 是否排除自动生成的对话摘要
 * @returns {object[]}
 */
function queryMemories(filters = {}) {
  // 后过滤：排除自动生成的对话摘要
  const _noAuto = filters.excludeAutoGenerated;
  const db = getDb();
  const conditions = [];
  const params = [];

  // 类型
  if (filters.type) {
    conditions.push('type = ?');
    params.push(filters.type);
  }

  // 深度
  if (filters.depth) {
    conditions.push('depth = ?');
    params.push(filters.depth);
  }

  // 来源
  if (filters.source) {
    conditions.push('source = ?');
    params.push(filters.source);
  }

  // 关键词（JSON 字符串 + 摘要 + 详情中模糊匹配）
  if (filters.keyword) {
    conditions.push("(keywords LIKE ? OR summary LIKE ? OR detail LIKE ?)");
    params.push(`%${filters.keyword}%`, `%${filters.keyword}%`, `%${filters.keyword}%`);
  }

  // 时间范围
  if (filters.timeFrom) {
    conditions.push('timestamp >= ?');
    params.push(filters.timeFrom);
  }
  if (filters.timeTo) {
    conditions.push('timestamp <= ?');
    params.push(filters.timeTo);
  }

  // 过期过滤
  if (!filters.includeExpired) {
    conditions.push(`(expire_at IS NULL OR expire_at > datetime('now'))`);
  }

  // 文本搜索（摘要 + 详情）
  if (filters.searchText) {
    conditions.push('(summary LIKE ? OR detail LIKE ?)');
    params.push(`%${filters.searchText}%`, `%${filters.searchText}%`);
  }

  const whereClause = conditions.length > 0 ? `WHERE ${conditions.join(' AND ')}` : '';
  const sql = `${whereClause} ORDER BY timestamp DESC LIMIT ? OFFSET ?`;

  const limit = filters.limit || 50;
  const offset = filters.offset || 0;
  params.push(limit, offset);

  let rows = db.prepare(`SELECT * FROM memories ${sql}`).all(...params);
  rows = rows.map(parseRow);

  // 后过滤：排除自动生成的对话摘要
  if (_noAuto) {
    rows = rows.filter(r => {
      const s = (r.summary || '').slice(0, 100);
      // autoSummary() 生成的摘要格式：'💡 对话摘要...' 或 '💗 对话摘要...'
      return !s.includes('对话摘要');
    });
  }

  return rows;
}

/**
 * 快速搜索入口：自动判断走浅度还是深度通道
 *
 * @param {string} query - 搜索词
 * @param {object} [options]
 * @param {boolean} [options.excludeAutoGenerated=false] - 是否排除自动生成的对话摘要
 * @returns {object[]}
 */
function search(query, options = {}) {
  if (!query || !query.trim()) return [];

  // 短查询、含精确关键词 → 走浅度通道（关键词 + 文本 LIKE）
  const isExact = query.length < 10 && !/[？。，、；：！…—]/.test(query);

  if (options.depth === 'shallow' || (isExact && !options.depth)) {
    let results = queryMemories({
      searchText: query,
      keyword: query,
      ...options,
    });
    // 浅搜也加上评分，方便排序和展示
    const queryTerms = tokenize(query);
    if (queryTerms.length > 0) {
      results = results.map(r => {
        const full = `${r.summary || ''} ${r.detail || ''}`;
        const docTerms = tokenize(full);
        return { ...r, score: cosineSimilarity(queryTerms, docTerms) };
      });
      results.sort((a, b) => b.score - a.score);
    }
    // 浅搜没结果时自动降级到深搜通道（jieba分词匹配，不要求连续）
    if (results.length === 0 && queryTerms.length > 0) {
      return searchDeep(query, options);
    }
    return results;
  }

  // 长查询 / 明确要求深度 → 走深度语义通道（初期用 TF-IDF 兜底）
  if (options.depth === 'deep' || (!isExact && !options.depth)) {
    return searchDeep(query, options);
  }

  return queryMemories({ searchText: query, ...options });
}

